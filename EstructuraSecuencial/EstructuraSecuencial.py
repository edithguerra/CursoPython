#ESTRUCTURA SECUENCIAL
#Cuando en un problema solo participan operaciones, entradas y salidas se le denomina estructura secuencial.
#los problemas diagramados y codificados previamente emplean solo estructuras secuenciales.
#EJEMPLO
#Realizar la carga de dos numeros enteros por teclado e imprmir su suma y su producto.
#  INICIO
#  NUM1
#  NUM2
#  SUMA=NUM1+NUM2
#  PRODUCTO=NUM1+NUM2
#  SUMA, PRODUCTO
#  FIN
#Tenemos dos entradas NUM1 y NUM2, dos operadores: la suma y el producto de los valores ingresados y dos salidas
#que son los resultados de la suma y el producto de los valores ingresados. En el simbolo de impresion
#podemos indicar una o mas salidas, eso queda a criterio del programador, lo mismo para indicar las entradas
